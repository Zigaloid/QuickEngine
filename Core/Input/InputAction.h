#pragma once

#include <string>
#include <string_view>
#include <functional>
#include <cstdint>

namespace Input {

// ?? Action Phases ?????????????????????????????????????????????????????????????

/** @brief Describes the lifecycle phase of an action event.
 *
 *  - Started   : The action began this frame (button just pressed, hold started).
 *  - Ongoing   : The action is in progress (e.g. a hold counting down).
 *  - Completed : The action reached its end condition (hold duration reached, double-tap confirmed).
 *  - Canceled  : The action was interrupted before completion (button released before hold duration).
 */
enum class ActionPhase
{
    Started,
    Ongoing,
    Completed,
    Canceled,
};

// ?? Action Value ??????????????????????????????????????????????????????????????

/** @brief The value payload carried by an ActionEvent.
 *
 *  For button-based actions, `triggered` is true and x/y are 0.
 *  For single-axis bindings, `x` carries the normalised axis value.
 *  For 2-D bindings (e.g. stick), both `x` and `y` are filled.
 *  `progress` is filled for Held triggers (0 ? 1 over holdDuration).
 */
struct ActionValue
{
    float x         = 0.f;
    float y         = 0.f;
    float progress  = 0.f; ///< [0,1] progress for Held triggers
    bool  triggered = false;
};

// ?? Action Event ??????????????????????????????????????????????????????????????

/** @brief An event dispatched to subscribers when an action fires. */
struct ActionEvent
{
    std::string_view actionName;
    ActionPhase      phase;
    ActionValue      value;
    double           deltaTime = 0.0;
};

// ?? Callback & Handle ?????????????????????????????????????????????????????????

using ActionCallback = std::function<void(const ActionEvent&)>;

/** @brief Opaque handle returned by InputActionManager::Subscribe / SubscribeAll.
 *  Pass back to Unsubscribe to remove the listener. */
struct ActionListenerHandle
{
    uint32_t    id         = 0;
    std::string actionName; ///< Empty = global (all actions)

    bool IsValid() const { return id != 0; }
};

} // namespace Input
